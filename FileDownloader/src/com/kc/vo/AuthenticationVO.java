package com.kc.vo;

public class AuthenticationVO {
	
	private String username;
	private String password;
	private String ip;
	private String url;
	private String serverDefaultUrl;
	private String serverScript;
	
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getIp() {
		return ip;
	}
	public void setIp(String ip) {
		this.ip = ip;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public String getServerDefaultUrl() {
		return serverDefaultUrl;
	}
	public void setServerDefaultUrl(String serverDefaultUrl) {
		this.serverDefaultUrl = serverDefaultUrl;
	}
	public String getServerScript() {
		return serverScript;
	}
	public void setServerScript(String serverScript) {
		this.serverScript = serverScript;
	}
	
	
	

}
